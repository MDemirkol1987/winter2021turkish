package day05_wrapperClass_Concatenation;

public class C4_ConditionalOperators {

	public static void main(String[] args) {
		//AND && , OR 
		
		boolean isTrue=5>4 && 7-3>0;
		System.out.println(isTrue);
		
	//AND ile baðlananlar hepsi true ise sonuç tru
	//bir tane bile false olsa sonuç false olur
		
		boolean isFalse= 5<4 || 7-3>0;
		System.out.println(isFalse);

	}

}
